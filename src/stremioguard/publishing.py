"""Shared compose-override generation for services published through gluetun."""

from __future__ import annotations

from stremioguard.config import CometConfig


def render_gluetun_ports(
    bind_addresses: list[str],
    port_mappings: list[tuple[int, int]],
) -> str | None:
    if not bind_addresses or not port_mappings:
        return None
    lines: list[str] = []
    for address in bind_addresses:
        for host_port, container_port in port_mappings:
            lines.append(f'      - "{address}:{host_port}:{container_port}"')
    return "\n".join(lines)


def render_stack_compose_override(
    *,
    bind_addresses: list[str],
    stremio_host_port: int,
    stremio_container_port: int,
    comet_config: CometConfig | None,
) -> str:
    port_mappings: list[tuple[int, int]] = [(stremio_host_port, stremio_container_port)]
    if comet_config and comet_config.enabled:
        port_mappings.append((comet_config.host_port, 8000))

    ports = render_gluetun_ports(bind_addresses, port_mappings)
    content = ["# Generated by ./stremio; do not edit by hand.", "services:", "  gluetun:"]
    if ports is None:
        content.append("    ports: []")
    else:
        content.extend(["    ports:", ports])

    if comet_config and comet_config.enabled:
        comet_volumes = [f"      - {comet_config.data_dir}:/app/data"]
        if comet_config.result_format_style != "emoji":
            comet_volumes.append(
                "      - "
                f"{comet_config.state_dir / 'formatting.py'}:/app/comet/utils/formatting.py:ro"
            )
        comet_volumes.append(
            f"      - {comet_config.state_dir / 'stream.py'}:/app/comet/api/endpoints/stream.py:ro"
        )
        comet_volumes.append(
            f"      - {comet_config.state_dir / 'torrentio.py'}:/app/comet/scrapers/torrentio.py:ro"
        )
        comet_volumes.append(
            f"      - {comet_config.state_dir / 'filtering.py'}:/app/comet/services/filtering.py:ro"
        )
        if comet_config.patch_episode_pack_results:
            orch_src = comet_config.state_dir / "orchestration.py"
            comet_volumes.append(f"      - {orch_src}:/app/comet/services/orchestration.py:ro")
        content.extend(
            [
                "  comet:",
                "    env_file:",
                f"      - {comet_config.runtime_env_file}",
                "    volumes:",
                *comet_volumes,
                "    healthcheck:",
                f"      interval: {comet_config.healthcheck_interval_seconds}s",
                "      timeout: 5s",
                "      retries: 5",
                "      start_period: 10s",
                "  comet-postgres:",
                "    volumes:",
                f"      - {comet_config.postgres_data_dir}:/var/lib/postgresql/",
            ]
        )

    return "\n".join(content) + "\n"
